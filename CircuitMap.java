public class CircuitMap {

    public CircuitMap() {
    }

    Layout layout;

    QubitType qubitType;

    public Layout getLayout() {
        return layout;
    }

    public void setLayout(Layout layout) {
        this.layout = layout;
    }

    public QubitType getQubitType() {
        return qubitType;
    }

    public void setQubitType(QubitType qubitType) {
        this.qubitType = qubitType;
    }

}